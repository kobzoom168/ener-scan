-- Knowledge base for the consult brain (อาจารย์เอเนอร์):
--   entry_type 'faq'       = scripted answer กบ approves — model must follow content verbatim-ish
--   entry_type 'knowledge' = domain lore the model rephrases in อาจารย์ voice
-- Retrieval is Thai keyword matching on question_patterns/tags (no embeddings yet).

create extension if not exists "pgcrypto";

create table if not exists public.kb_entries (
  id uuid primary key default gen_random_uuid(),
  entry_type text not null default 'knowledge',
  title text not null,
  question_patterns text not null default '',
  answer text not null,
  tags text not null default '',
  status text not null default 'active',
  source text not null default 'manual',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint kb_entries_title_key unique (title),
  constraint kb_entries_entry_type_check check (entry_type in ('faq', 'knowledge')),
  constraint kb_entries_status_check check (status in ('active', 'disabled'))
);

create index if not exists idx_kb_entries_status on public.kb_entries (status);

drop trigger if exists trg_kb_entries_set_updated_at on public.kb_entries;
create trigger trg_kb_entries_set_updated_at
  before update on public.kb_entries
  for each row execute function public.set_updated_at();

